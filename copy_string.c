/**
 **_strncpy - This copies a string
 *@dest:denotes the destination string to be copied to
 *@src: denotes the source string
 *@n: denotes the amount of characters to be copied
 *Return: returns the concatenated string
 */
char *_strncpy(char *dest, char *src, int n)
{
	int i, j;
	char *s = dest;

	i = 0;
	while (src[i] != '\0' && i < n - 1)
	{
		dest[i] = src[i];
		i++;
	}
	if (i < n)
	{
		j = i;
		while (j < n)
		{
			dest[j] = '\0';
			j++;
		}
	}
	return (s);
}

/**
 **_strncat - This concatenates two strings
 *@dest: denotes the first string
 *@src: denotes the second string
 *@n: denotes the amount of bytes to be maximally used
 *Return: returns the concatenated string
 */
char *_strncat(char *dest, char *src, int n)
{
	int i, j;
	char *s = dest;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (src[j] != '\0' && j < n)
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	if (j < n)
		dest[i] = '\0';
	return (s);
}

/**
 **_strchr - This locates a character in a string
 *@s: denotes the string to be parsed
 *@c: denotes the character to look for
 *Return: returns (s) a pointer to the memory area s
 */
char *_strchr(char *s, char c)
{
	do {
		if (*s == c)
			return (s);
	} while (*s++ != '\0');

	return (NULL);
}
